package com.loggitorBE.loggitorBE.domain;


public class AppsNames {
	
	// pojo class that define the list of applications names

	String appName;
	
	public AppsNames(){}
	
	
	public AppsNames(String appName) {
		
		super();
		this.appName=appName;
		}


	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}
	
	



}
